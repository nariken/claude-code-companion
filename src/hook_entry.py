"""Claude Code hook entrypoint. Wired into ~/.claude/settings.json by install.sh.

  hook_entry.py session_end   <- SessionEnd / PreCompact: spawn detached
                                 handoff generation and return immediately
                                 (hooks must not block Claude Code).
  hook_entry.py session_start <- SessionStart: emit latest handoff as
                                 additionalContext so new sessions start
                                 with the handover already loaded.
"""
import json
import os
import subprocess
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from common import (HOOK_GUARD_ENV, L, get_config, handoff_meta_path,
                    handoff_path, log, read_json)

MAX_INJECT_CHARS = 12000


def _read_stdin_json() -> dict:
    try:
        return json.loads(sys.stdin.read() or "{}")
    except json.JSONDecodeError:
        return {}


def on_session_end(payload: dict):
    cwd = payload.get("cwd") or os.getcwd()
    transcript = payload.get("transcript_path", "")
    session_id = payload.get("session_id", "unknown")
    trigger = payload.get("hook_event_name", "SessionEnd")
    reason = payload.get("reason") or payload.get("trigger") or ""
    if not transcript or not Path(transcript).exists():
        return
    # Detached: survives Claude Code exiting, doesn't hold the hook open.
    script = Path(__file__).resolve().parent / "handoff.py"
    subprocess.Popen(
        [sys.executable, str(script), cwd, transcript, session_id,
         f"{trigger}:{reason}" if reason else trigger],
        stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
        stdin=subprocess.DEVNULL, start_new_session=True)
    log("hook", f"spawned handoff gen for {cwd} ({trigger})")


def on_session_start(payload: dict):
    cwd = payload.get("cwd") or os.getcwd()
    hpath = handoff_path(cwd)
    if not hpath.exists():
        return
    meta = read_json(handoff_meta_path(cwd), {}) or {}
    generated_at = meta.get("generated_at", "")
    if generated_at:
        import datetime
        try:
            dt = datetime.datetime.fromisoformat(generated_at)
            age_h = (datetime.datetime.now() - dt).total_seconds() / 3600
            if age_h > get_config()["stale_handoff_days"] * 24:
                return
        except ValueError:
            pass
    try:
        content = hpath.read_text()[:MAX_INJECT_CHARS]
    except OSError:
        return
    context = (
        L("前回セッションからの自動引き継ぎメモ(aiagent-controlが生成)です。"
          "作業を継続する場合はこれを前提にしてください。内容が現状と食い違う場合は現状を優先してください。\n\n",
          "Automatic handoff notes from the previous session (generated by aiagent-control). "
          "Use this as context when continuing work. If it conflicts with the current state "
          "of the code, trust the current state.\n\n")
        + content)
    print(json.dumps({
        "hookSpecificOutput": {
            "hookEventName": "SessionStart",
            "additionalContext": context,
        }
    }, ensure_ascii=False))
    log("hook", f"injected handoff into new session for {cwd}")


def main():
    if os.environ.get(HOOK_GUARD_ENV):
        return  # our own internal claude subprocess — never recurse
    mode = sys.argv[1] if len(sys.argv) > 1 else ""
    payload = _read_stdin_json()
    try:
        if mode == "session_end":
            on_session_end(payload)
        elif mode == "session_start":
            on_session_start(payload)
    except Exception as e:  # a broken hook must never break Claude Code
        log("hook", f"ERROR in {mode}: {e}")


if __name__ == "__main__":
    main()
